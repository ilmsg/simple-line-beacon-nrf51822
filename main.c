{
    uint32_t err_code;
    // do nothing
}

/**@brief Function for initializing the BLE stack.
 *
 * @details Initializes the SoftDevice and the BLE event interrupt.
 */
static void ble_stack_init(void)
{
    uint32_t err_code;

    nrf_clock_lf_cfg_t rc_cfg = {
        .source = NRF_CLOCK_LF_SRC_RC,
        .rc_ctiv = 16, // Check temperature every 4 seconds
        .rc_temp_ctiv = 2, // Calibrate at least every 8 seconds even if the temperature hasn't changed
    };
    SOFTDEVICE_HANDLER_INIT(&rc_cfg, NULL);

    ble_enable_params_t ble_enable_params;
    err_code = softdevice_enable_get_default_config(CENTRAL_LINK_COUNT,
                                                    PERIPHERAL_LINK_COUNT,
                                                    &ble_enable_params);
    APP_ERROR_CHECK(err_code);

    // Check the ram settings against the used number of links
    CHECK_RAM_START_ADDR(CENTRAL_LINK_COUNT, PERIPHERAL_LINK_COUNT);

    err_code = softdevice_enable(&ble_enable_params);
    APP_ERROR_CHECK(err_code);
}

#define APP_ADV_DATA_LENGTH      0x0E
#define APP_LINE_FRAME_TYPE      0x02
#define APP_LINE_HWID            0x01, 0x00, 0x63, 0xc1, 0x14
#define APP_LINE_TXPOWER         0x7F
#define APP_LINE_MESSAGE         0x12, 0x34, 0x56, 0x78, 0x9a, 0xbc, 0xde

static uint8_t m_beacon_info[APP_ADV_DATA_LENGTH] = 
{
    APP_LINE_FRAME_TYPE,
    APP_LINE_HWID,
    APP_LINE_TXPOWER,
    APP_LINE_MESSAGE
};

/**@brief Function for initializing the Advertising functionality.
 */
static void advertising_init(void)
{
    uint32_t               err_code;
    ble_advdata_t          advdata;
    ble_adv_modes_config_t options;

    // Build advertising data struct to pass into @ref ble_advertising_init.
    memset(&advdata, 0, sizeof(advdata));

    ble_advdata_service_data_t service_data;
    service_data.data.p_data = (uint8_t *) m_beacon_info;
    service_data.service_uuid = 0xFE6F;
    service_data.data.size   = APP_ADV_DATA_LENGTH;

    advdata.name_type               = BLE_ADVDATA_NO_NAME;
    advdata.include_appearance      = true;
    advdata.flags                   = BLE_GAP_ADV_FLAGS_LE_ONLY_GENERAL_DISC_MODE;
    advdata.uuids_complete.uuid_cnt = sizeof(m_adv_uuids) / sizeof(m_adv_uuids[0]);
    advdata.uuids_complete.p_uuids  = m_adv_uuids;
    advdata.p_service_data_array = &service_data;
    advdata.service_data_count = 1;

    memset(&options, 0, sizeof(options));
    options.ble_adv_fast_enabled  = true;
    options.ble_adv_fast_interval = APP_ADV_INTERVAL;
    options.ble_adv_fast_timeout  = APP_ADV_TIMEOUT_IN_SECONDS;

    err_code = ble_advertising_init(&advdata, NULL, &options, on_adv_evt, NULL);
    APP_ERROR_CHECK(err_code);
}

/**@brief Function for the Power manager.
 */
static void power_manage(void)
{
    uint32_t err_code = sd_app_evt_wait();

    APP_ERROR_CHECK(err_code);
}


/**@brief Function for starting advertising.
 */
static void advertising_start(void)
{
    uint32_t err_code = ble_advertising_start(BLE_ADV_MODE_FAST);

    APP_ERROR_CHECK(err_code);
}


/**@brief Function for application main entry.
 */
int main(void)
{
    uint32_t err_code;

    // Initialize.
    err_code = NRF_LOG_INIT(NULL);
    APP_ERROR_CHECK(err_code);

    timers_init();
    ble_stack_init();
    gap_params_init();
    advertising_init();

    // Start execution.
    err_code = ble_advertising_start(BLE_ADV_MODE_FAST);
    APP_ERROR_CHECK(err_code);

    // Enter main loop.
    for (;;)
    {
        if (NRF_LOG_PROCESS() == false)
        {
            power_manage();
        }
    }
}


